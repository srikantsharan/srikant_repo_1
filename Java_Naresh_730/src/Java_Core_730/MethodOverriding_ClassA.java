package Java_Core_730;

public class MethodOverriding_ClassA {
	public void Automation(int a,int b)
	{
		int s=a+b;
		System.out.println(s);
	}
	public void Automation(String name,String name1)
	{
		System.out.println(name);
		System.out.println(name1);
	}
	public void Automation(String name3,int c)
	{
		System.out.println(name3);
		System.out.println(c);
	}
	

}
