package Java_Core_730;

public class Else_If_Condition {

	public static void main(String[] args) {
	int a=45+67;
	if(a>345)
	{
		System.out.println("A1");
	}
	else if(a>564)
	{
		System.out.println("A2");
	}
	else if(a==116)
	{
		System.out.println("A3");
	}
	else if(a<234)
	{
		System.out.println("A4");
	}
	else
	{
		System.out.println("A5");
	}

	}

}
